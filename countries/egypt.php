    Egypt 
Late May 1882
Crossing Indian Ocean to the Cape of Guardafui, Africa (Somalia)
Rizal sighted the barren coast of Africa, which he called an inhospitable land but famous.
British Aden (Yemen)
Rizal went ashore to see the sights. He found the city hotter than Manila. He was amused to see the camels for the first time.
City of Suez (Red Sea terminal of the Suez Canal)
Rizal disembarked and went sightseeing, and was most impressed by the beautiful moonlight, which reminded him of Calamba and his family.
Djemnah took five days to traverse through the Suez Canal, which was built by Ferdinand de Lesseps, a French diplomat-engineer, and was inaugurated on November 17, 1869.
Port Said
Rizal landed in Port Said, the Mediterranean terminal of the Suez Canal, to see the interesting sights. He was fascinated by the multi-racial inhabitants, speaking a babel of tongues like Arabic, Egyptian, Greek, French, Italian, Spanish, etc.

