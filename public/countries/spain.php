    <h1 class="elegant-title">Spain</h1>
    <h2 class="vintage-subtitle">June 16, 1882</h2>
    <div class="text-columns single-col">
        <p>
            <span class="drop-cap">O</span>
            n the afternoon of June 15, 1882, Rizal left Marseille by train. </br> </br>
            Rizal crossed the Pyrenees and stopped for a day at the frontier town of Port Bou, where he noticed the indifference of Spanish immigration officers to tourists, unlike the French immigration officers. </br> </br>
            On June 16, 1882, Rizal reached his destination, Barcelona, by rail.
            Rizal’s first impression of Barcelona, the greatest city of Cataluña and Spain’s second largest city, was ugly and dirty due to the little inns and inhospitable residents.
            Rizal happened to stay upon his arrival at a dark and dirty inn located on an unimpressive narrow street in the town’s most ugly side, where staff and guests were indifferent to him. </br> </br>
            Later, Rizal found the city to be great with its atmosphere of freedom and liberalism. Its people were open-hearted, hospitable, and courageous.
            Rizal enjoyed promenading along Las Ramblas, the most famous street in Barcelona.
            Rizal was welcomed by his schoolmates in Ateneo. They gave him a party at their favorite cafe in Plaza de Cataluña, where they had an exchange of toasts and told him about the attractions of Barcelona and the customs of the Spanish people.
        </p>
