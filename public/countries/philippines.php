    <h2 class="vintage-subtitle">May 3, 1882</h2>
    <h2 class="vintage-subtitle">Port of Manila, Philippines</h2>

    <div class="text-columns single-col">
        <p>
            <span class="drop-cap">R</span>
            izal secretly leaves the Philippines for the first time on the Spanish steamer Salvadora, bound for Singapore, traveling under the name “Jose Mercado” to avoid suspicion from Spanish authorities and friars. </br> </br>
            His “secret mission” is to study in Spain, observe European societies, and prepare himself intellectually to work for reforms in the Philippines. </br> </br>
            Before his departure, he wrote a farewell letter for his beloved parents and sweetheart Leonor Rivera.
            His departure was only known by his brother Paciano, his uncle Antonio Rivera, his sisters Neneng and Lucia, the Valenzuela family, Pedro A. Paterno, his compadre Mateo Evangelista, the Ateneo Jesuit fathers, and some intimate friends, including Chengoy or Jose M. Cecilio. </br> </br>
            The Jesuit priests gave him letters of recommendation to the members of their Society in Barcelona.
        </p>
